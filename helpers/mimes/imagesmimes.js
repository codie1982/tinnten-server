// helpers/allowedImageMimeTypes.js

module.exports = [
    'image/jpeg',        // .jpg, .jpeg
    'image/png',         // .png
    'image/gif',         // .gif
    'image/bmp',         // .bmp
    'image/svg+xml',     // .svg
    'image/webp',        // .webp
    'image/tiff',        // .tiff
    'image/heic',        // .heic
    'image/apng',        // .apng
    'image/x-icon',      // .ico
    'image/vnd.microsoft.icon' // .ico
];

// •	image/jpeg: JPEG formatındaki resimler için kullanılır (.jpg, .jpeg).
// •	image/png: PNG formatındaki resimler için kullanılır (.png).
// •	image/gif: GIF formatındaki resimler için kullanılır (.gif).
// •	image/bmp: BMP formatındaki resimler için kullanılır (.bmp).
// •	image/svg+xml: SVG formatındaki vektörel resimler için kullanılır (.svg).
// •	image/webp: WebP formatındaki resimler için kullanılır (.webp).
// •	image/tiff: TIFF formatındaki resimler için kullanılır (.tiff).
// •	image/heic: HEIC formatındaki resimler için kullanılır (.heic).
// •	image/apng: Animated PNG formatındaki resimler için kullanılır (.apng).
// •	image/x-icon ve image/vnd.microsoft.icon: ICO formatındaki resimler için kullanılır (.ico).